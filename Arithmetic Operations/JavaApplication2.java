package javaapplication2;

public class JavaApplication2 {

    public static void main(String[] args) {
        
        // print something
        System.out.println("EKSE!!");
        
        System.out.println("--------------");
        
        // calculations of data types
        int x = 10;
        int y = 20;
        int result = x + y;
        System.out.println("result = " + result);
        
        System.out.println("--------------");
        
        // modulus(%) ::: remainder
        int a = 10;
        int b = 3;
        int total = a / b;
        int remainder = a % b;
        System.out.println("total = " + total + "\nremainder = " + remainder);
    }
    
}
